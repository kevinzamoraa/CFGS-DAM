    private void btnEqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqActionPerformed
        numIntroducido2 = Double.parseDouble(outputResultado.getText());
        if (op == "+") {
            resultado = numIntroducido1 + numIntroducido2;
            outputResultado.setText(String.valueOf(resultado));
        } else if (op == "-") {
            resultado = numIntroducido1 - numIntroducido2;
            outputResultado.setText(String.valueOf(resultado));
        } else if (op == "*") {
            resultado = numIntroducido1 * numIntroducido2;
            outputResultado.setText(String.valueOf(resultado));
        } else if (op == "/") {
            resultado = numIntroducido1 / numIntroducido2;
            outputResultado.setText(String.valueOf(resultado));
        }
    }//GEN-LAST:event_btnEqActionPerformed

